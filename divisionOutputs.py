# Description: This file contains the code to demonstrate the outputs of division operations in Python.
def divisionOutputs():

    result = 10 / 5
    print(result)  # outputs 2.0
    print(type(result))  # outputs <class 'float'>

    result = 10 // 5
    print(result)  # outputs 2
    print(type(result))  # outputs <class 'int'>

    result = 10 % 5
    print(result)  # outputs 0
    print(type(result))  # outputs <class 'int'>

    result = 10 / 4
    print(result)  # outputs 2.5
    print(type(result))  # outputs <class 'float'>

    result = 10 // 4
    print(result)  # outputs 2
    print(type(result))  # outputs <class 'int'>

    result = 10 % 4
    print(result)  # outputs 2
    print(type(result))  # outputs <class 'int'>


divisionOutputs()

